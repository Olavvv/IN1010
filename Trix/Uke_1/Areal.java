class Areal {
    public static void main (String[] args)
    {
        double bredde = 10.0;
        double lengde = 20.0;

        System.out.println("Bredde: " + bredde);
        System.out.println("Lengde: " + lengde);

        double areal = bredde * lengde;
        System.out.println("Areal: " + areal);
    }
}
